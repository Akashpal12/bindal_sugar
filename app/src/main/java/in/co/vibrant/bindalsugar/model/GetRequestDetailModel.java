package in.co.vibrant.bindalsugar.model;

public class GetRequestDetailModel {

    private Integer id;
    private Integer supcode;
    private String supname;
    private String phone;
    private String vsdate;
    private String rsdate;
    private String reason;
    private String rDesc;
    private String reqdate;
    private String status;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getSupcode() {
        return supcode;
    }

    public void setSupcode(Integer supcode) {
        this.supcode = supcode;
    }

    public String getSupname() {
        return supname;
    }

    public void setSupname(String supname) {
        this.supname = supname;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getVsdate() {
        return vsdate;
    }

    public void setVsdate(String vsdate) {
        this.vsdate = vsdate;
    }

    public String getRsdate() {
        return rsdate;
    }

    public void setRsdate(String rsdate) {
        this.rsdate = rsdate;
    }

    public String getReason() {
        return reason;
    }

    public void setReason(String reason) {
        this.reason = reason;
    }

    public String getRDesc() {
        return rDesc;
    }

    public void setRDesc(String rDesc) {
        this.rDesc = rDesc;
    }

    public String getReqdate() {
        return reqdate;
    }

    public void setReqdate(String reqdate) {
        this.reqdate = reqdate;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
