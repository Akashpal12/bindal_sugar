package in.co.vibrant.bindalsugar.model;

public class StaffVillageActivityPending {



    protected String id;
    protected String villCode;
    protected String villName;
    protected String growerCount;
    protected String plotCount;
    protected String color;
    protected String textColor;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getVillCode() {
        return villCode;
    }

    public void setVillCode(String villCode) {
        this.villCode = villCode;
    }

    public String getVillName() {
        return villName;
    }

    public void setVillName(String villName) {
        this.villName = villName;
    }

    public String getGrowerCount() {
        return growerCount;
    }

    public void setGrowerCount(String growerCount) {
        this.growerCount = growerCount;
    }

    public String getPlotCount() {
        return plotCount;
    }

    public void setPlotCount(String plotCount) {
        this.plotCount = plotCount;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getTextColor() {
        return textColor;
    }

    public void setTextColor(String textColor) {
        this.textColor = textColor;
    }
}
