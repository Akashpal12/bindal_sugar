package in.co.vibrant.bindalsugar.model;

public class SupervisorSummary {



    private String supervisorCode;
    private String supervisorName;
    private String onDatePlots;
    private String onDateArea;
    private String totalPlot;
    private String totalArea;
    private String color;
    private String textColor;


    public String getSupervisorCode() {
        return supervisorCode;
    }

    public void setSupervisorCode(String supervisorCode) {
        this.supervisorCode = supervisorCode;
    }

    public String getSupervisorName() {
        return supervisorName;
    }

    public void setSupervisorName(String supervisorName) {
        this.supervisorName = supervisorName;
    }

    public String getOnDatePlots() {
        return onDatePlots;
    }

    public void setOnDatePlots(String onDatePlots) {
        this.onDatePlots = onDatePlots;
    }

    public String getOnDateArea() {
        return onDateArea;
    }

    public void setOnDateArea(String onDateArea) {
        this.onDateArea = onDateArea;
    }

    public String getTotalPlot() {
        return totalPlot;
    }

    public void setTotalPlot(String totalPlot) {
        this.totalPlot = totalPlot;
    }

    public String getTotalArea() {
        return totalArea;
    }

    public void setTotalArea(String totalArea) {
        this.totalArea = totalArea;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getTextColor() {
        return textColor;
    }

    public void setTextColor(String textColor) {
        this.textColor = textColor;
    }
}
