package in.co.vibrant.bindalsugar.model;

public class RemedialJsonDataModel {

    private String REMICODE;
    private String REMIDIALNAME;
    private String DAYES;
    private String STATUS;

    public String getREMICODE() {
        return REMICODE;
    }

    public void setREMICODE(String REMICODE) {
        this.REMICODE = REMICODE;
    }

    public String getREMIDIALNAME() {
        return REMIDIALNAME;
    }

    public void setREMIDIALNAME(String REMIDIALNAME) {
        this.REMIDIALNAME = REMIDIALNAME;
    }

    public String getDAYES() {
        return DAYES;
    }

    public void setDAYES(String DAYES) {
        this.DAYES = DAYES;
    }

    public String getSTATUS() {
        return STATUS;
    }

    public void setSTATUS(String STATUS) {
        this.STATUS = STATUS;
    }
}
